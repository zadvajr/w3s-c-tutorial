//This example shows c data types

#include <stdio.h>

int main() {
    //variable declarations and initializations
    int myInteger = 34;
    float myFloat = 22.0;
    char myChar = 'Z';
    double myDouble = 22.34000;

    //prints variable
    printf("My Integer is: %d\n", myInteger);
    printf("My Float is: %f\n", myFloat);
    printf("My Character is: %c\n", myChar);
    printf("My Double is: %lf\n", myDouble);

    return 0;
}